import xml.etree.ElementTree as ET
from pathlib import Path

import pandas as pd


def parse_fcd_output(fcd_output: Path) -> pd.DataFrame:
    """
    Parse the XML FCD output file generated by the simulation and extract vehicle data.

    Args:
        fcd_output (Path): Path to the XML FCD output file.

    Returns:
        pd.DataFrame: DataFrame containing vehicle data with columns:
            - timestep_time: Time of the timestep in seconds.
            - vehicle_id: Unique identifier for each vehicle.
            - x: X-coordinate of the vehicle's position.
            - y: Y-coordinate of the vehicle's position.
            - speed_kmh: Speed of the vehicle in km/h.
            - acceleration: Acceleration of the vehicle in m/s^2.
            - odometer: Distance traveled by the vehicle in meters.
    """
    data = []
    context = ET.iterparse(fcd_output, events=("end",))

    for _, element in context:
        if element.tag == "timestep":
            time = float(element.get("time"))

            for vehicle in element.findall("vehicle"):
                data.append(
                    {
                        "timestep_time": time,
                        "vehicle_id": vehicle.get("id"),
                        "x": float(vehicle.get("x")),
                        "y": float(vehicle.get("y")),
                        "speed_kmh": float(vehicle.get("speed")) * 3.6,
                        "acceleration": float(vehicle.get("acceleration")),
                        "odometer": float(vehicle.get("odometer")),
                    }
                )
            element.clear()

    return pd.DataFrame(data)


def preprocess_fcd(df_fcd: pd.DataFrame) -> pd.DataFrame:
    """
    Remove nulls and records beyond 10 hours.

    Args:
        df_fcd (pd.DataFrame): DataFrame containing vehicle data that must have `timestep_time`, `speed_kmh`, and `vehicle_id` columns.

    Returns:
        pd.DataFrame: Preprocessed DataFrame with non-null rows and timesteps under 10 hours.
    """
    df_fcd_clean = df_fcd.dropna().reset_index(drop=True)
    df_fcd_clean = df_fcd_clean[df_fcd_clean["timestep_time"] < 36000]
    return df_fcd_clean


def report_fcd_stats(df_fcd: pd.DataFrame) -> None:
    """
    Print basic statistics for an FCD DataFrame.

    Args:
        df_fcd (pd.DataFrame): DataFrame containing vehicle data that must have `speed_kmh`, `vehicle_id` and `odometer` columns.
    """
    data_shape = df_fcd.shape
    average_speed = df_fcd["speed_kmh"].mean()
    average_trip_distance = df_fcd.groupby("vehicle_id")["odometer"].max().mean()
    unique_vehicles_count = df_fcd["vehicle_id"].nunique()

    print(f"Data shape: {data_shape}")
    print(f"Average speed: {average_speed:.2f} km/h")
    print(f"Average trip distance: {average_trip_distance:.2f} m")
    print(f"Number of unique vehicles: {unique_vehicles_count}")


def aggregate_fcd(df_fcd: pd.DataFrame) -> tuple[pd.DataFrame, pd.DataFrame]:
    """
    Produce per-second and per-hour summaries of FCD DataFrame.

    Args:
        df_fcd (pd.DataFrame): DataFrame containing vehicle data that must have `timestep_time`, `speed_kmh`, and `vehicle_id` columns.

    Returns:
        tuple[pd.DataFrame, pd.DataFrame]:
            - df_per_second: DataFrame containing average speed and vehicle count per second.
            - df_per_hour: DataFrame containing average speed and vehicle count per hour.
    """
    df_fcd["second"] = df_fcd["timestep_time"].astype(int)
    df_fcd["hour"] = (df_fcd["timestep_time"] // 3600).astype(int)

    df_per_second = (
        df_fcd.groupby("second")
        .agg(average_speed_kmh=("speed_kmh", "mean"), vehicle_count=("vehicle_id", "nunique"))
        .reset_index()
    )
    df_per_hour = (
        df_fcd.groupby("hour")
        .agg(average_speed_kmh=("speed_kmh", "mean"), vehicle_count=("vehicle_id", "nunique"))
        .reset_index()
    )

    df_fcd = df_fcd.drop(columns=["second", "hour"])

    return df_per_second, df_per_hour
